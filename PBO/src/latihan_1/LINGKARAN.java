package latihan_1;

public class LINGKARAN {
	double phi = 3.14;
	public double Luas (double jari) {
		double Luas = phi*(jari*jari);
		return Luas;
	}
	public double keliling(double jari) {
		double Keliling = 2*phi*jari;
		return Keliling;
	}
}
